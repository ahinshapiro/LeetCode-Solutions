class Solution {
    public int canCompleteCircuit(int[] gas, int[] cost) {
        for(int i = 0 ;i < gas.length ; i++){
            if(gas[i] >= cost[i]){
                if(getPath(gas,cost,i)) return i;
            }
        }
        return -1;
    }
    public boolean getPath(int[] gas, int[] cost, int pos)
    {
        int path = 0;
        int i = pos;
        int j = 0;
        path += gas[i++];
        int flag = 0;
        while(j <= gas.length)
        {
            flag ++;
            if(i == gas.length){
                i = 0;
            }
            if( i > 0 ){
                if(path - cost[i-1] < 0) return false;
                path = path - cost[i-1] + gas[i];
            }
            else{
                if(path - cost[gas.length-1] < 0) return false;
                path = path - cost[gas.length-1] + gas[i];
            }
            i++;
            j++;
        }
        if( flag == gas.length+1 ) return true;
        return false;
    }
}

